x = 1
y = 2.0
z = "3"

print("before typecasting.")
print(x, y, z)

print("converting them to string")
print(str(x), str(y), z)

print("converting them to float")
print(float(x), y, float(z))

print("converting them to int")
print(x, int(y), int(z))

age = 52
print("The age of baby boy is " + str(age))   # typecasting is required here.